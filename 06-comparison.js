//Operaciones de comparación en JS: Igualdad y desigualdad

//Igualdad debil (==)

console.log (5 == '5'); //true
console.log (true == 1);  //true
console.log (false == 0); //true
console.log (null == undefined); //true

//Desigualdad debil (!=)


console.log (5 != '5'); 
console.log (true != 1);  
console.log (false != 0); 
console.log (null != undefined);

//Igualdad estricta (===)

console.log (5 === '5'); 
console.log (5 === '5');

console.log (5 !== 5);
console.log (5 !== '5');

